package com.javaSe.forT;

public class ForT2 {
    public static void main(String[] args) {
        int i = 5;
        if (i < 10) {
            System.out.println("<10");
        }
        else if (i < 20 && i >= 10) {
            System.out.println("10-20");
        }
        else if (i < 30 && i >= 20) {
            System.out.println("20-30");
        }
    }
}
